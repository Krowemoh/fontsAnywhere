/* Get key events, specifically the enter key */
function getKey(e,keypress)
{
  if (keypress) //if the key is pressed and its one of these
  {
    var input = document.getElementById("fontInput").value;
    if (e.keyCode ==13) //enter
    {
      changeFont(input);
    }
    else if (e.keyCode ==40) //down
    {
      getNext(input,1);
    }
    else if(e.keyCode ==38) //up
    {
      getNext(input,-1);
    }
  }
  else
  {
    if(e.keyCode!=40 && e.keyCode!=38) //if the key is up and its not the arrows
    {
      searchDict();
    }
  }
}
